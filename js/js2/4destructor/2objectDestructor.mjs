// Object Destructuring
const teacher = { name: "nitan", age: 30, isMarried: true };
console.log(teacher.name);
console.log(teacher.age);
console.log(teacher.isMarried);

// Object destructor

const { name, age, isMarried } = { name: "nitan", age: 30, isMarried: true };

console.log(name);
console.log(age);
console.log(isMarried);
