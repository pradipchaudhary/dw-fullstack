// function with parameter
const parameterFunction = function (number1, number2, number3) {
    // parameter
    console.log(
        `The parameter function values are : ${number1}, ${number2} and ${number3}`
    );
};
parameterFunction(2, 4, 6); //number are called argument

// it is not necessary to have same number of argument and parameter
